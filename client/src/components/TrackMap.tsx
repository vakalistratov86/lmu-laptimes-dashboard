export function TrackMap({ name, className = "" }: { name: string; className?: string }) {
  const d = PATHS[name];
  if (!d) return null;
  return (
    <svg
      viewBox="0 0 420 250"
      className={className}
      fill="none"
      aria-label={`Схема трассы ${name}`}
      role="img"
      data-testid="track-map"
    >
      {/* фоновая «тень» контура */}
      <path
        d={d}
        stroke="currentColor"
        strokeWidth={16}
        strokeLinecap="round"
        strokeLinejoin="round"
        className="opacity-10"
      />
      {/* основная линия трассы */}
      <path
        d={d}
        stroke="currentColor"
        strokeWidth={5}
        strokeLinecap="round"
        strokeLinejoin="round"
      />
      {/* штрихпунктир осевой */}
      <path
        d={d}
        stroke="hsl(var(--background))"
        strokeWidth={1.4}
        strokeLinecap="round"
        strokeLinejoin="round"
        strokeDasharray="2 8"
      />
    </svg>
  );
}

export function hasTrackMap(name: string): boolean {
  return name in PATHS;
}
